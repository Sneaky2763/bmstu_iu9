class Point{
    private double x, y, z;
    public Point(double x, double y, double z){
        this.x=x;
        this.y=y;
        this.z=z;
    }
    public double getx(){
        return this.x;
    }
    public double gety(){
        return this.y;
    }
    public double getz(){
        return this.z;
    }
}
